#!/usr/bin/python3

"""A python script that calculates the simple interest of principal amount,
ROI and time"""

# Simple interest formular (I = P * R * T)
# I = Interest earned
# P = Principal amount (initial amount)
# R = Rate (Annual interest rate)
# T = Time taken

principal = 1000
rate = 0.05
time = 3
interest = principal * rate * time

print(f'The simple interest is: {interest}')
