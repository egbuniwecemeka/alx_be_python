#!/usr/bin/env python3

"""Performs basic arithmetic operations"""

# Addition
def add(a, b):
    """Returns the sum of numbers"""
    return a + b

# Subtraction
def subtract(a, b):
    """Returns the difference between two numbers"""
    return a - b

# Multiplication
def multiply(a, b):
    """Returns the product of two numbers"""
    return a * b

# Division
def divide(a, b):
    """Returns the remainder f two numbers"""
    return a / b
