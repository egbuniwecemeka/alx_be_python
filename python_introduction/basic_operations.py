#!/usr/bin/python3
"""Python script performing basic arithmetic operations,
and prints their results."""

# Variables
number1 = 10
number2 = 5

# Arithmetic operations
num_sum = number1 + number2
difference = number1 - number2
product = number1 * number2

# Printing values to outputs
print(f'Addition of {number1} and {number2} is {num_sum}')
print(f'Subtraction of {number1} and {number2} is {difference}')
print(f'Multiplication of {number1} and {number2} is {product}')
